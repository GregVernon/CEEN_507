function [P,W]=Quadrature(Family,n)
if Family=="Gauss-Legendre"
    if n==1
        P=0;
        W=2;
    elseif n==2
        P=[-1/sqrt(3) 1/sqrt(3)];
        W=[1 1];
    elseif n==3
        P=[-sqrt(3/5) 0 sqrt(3/5)];
        W=[5/9 8/9 5/9];
    elseif n==4
        P=[-sqrt(3/7+2/7*sqrt(6/5))...
            -sqrt(3/7-2/7*sqrt(6/5))...
            sqrt(3/7-2/7*sqrt(6/5))...
            sqrt(3/7+2/7*sqrt(6/5))];
        W=[(18-sqrt(30))/36 ...
            (18+sqrt(30))/36 ...
            (18+sqrt(30))/36 ...
            (18-sqrt(30))/36];
    elseif n==5
        P=[-1/3*sqrt(5+2*sqrt(10/7)) ...
            -1/3*sqrt(5-2*sqrt(10/7)) ...
            0 ...
            1/3*sqrt(5-2*sqrt(10/7)) ...
            1/3*sqrt(5+2*sqrt(10/7))];
        W=[(322-13*sqrt(70))/900 ...
            (322+13*sqrt(70))/900 ...
            128/225 ...
            (322+13*sqrt(70))/900 ...
            (322-13*sqrt(70))/900];
    end
end
function [P,W]=Quadrature(Family,n)
if Family=="Gauss-Legendre"
    if n == 1
        P = sym(0);
        W = sym(2);
    elseif n == 2
        P = [str2sym("-1/sqrt(3)"); ...
              str2sym("1/sqrt(3)]")];
        
        W = [sym(1); ...
             sym(1)];
    elseif n == 3
        P = [str2sym("-sqrt(3/5)"); ... 
              str2sym("0"); ...
              str2sym("sqrt(3/5)]")];
          
        W = [str2sym("5/9"); ...
             str2sym("8/9"); ...
             str2sym("5/9]")];
    elseif n == 4
        P = [str2sym("-sqrt(3/7+2/7*sqrt(6/5))"); ...
             str2sym("-sqrt(3/7-2/7*sqrt(6/5))"); ...
             str2sym("sqrt(3/7-2/7*sqrt(6/5))"); ...
             str2sym("sqrt(3/7+2/7*sqrt(6/5))")];
         
        W = [str2sym("(18-sqrt(30))/36"); ...
             str2sym("(18+sqrt(30))/36"); ...
             str2sym("(18+sqrt(30))/36"); ...
             str2sym("(18-sqrt(30))/36")];
    elseif n == 5
        P = [str2sym("-1/3*sqrt(5+2*sqrt(10/7))"); ...
             str2sym("-1/3*sqrt(5-2*sqrt(10/7))"); ...
              str2sym("0"); ...
              str2sym("1/3*sqrt(5-2*sqrt(10/7))"); ...
              str2sym("1/3*sqrt(5+2*sqrt(10/7))")];
          
        W = [str2sym("(322-13*sqrt(70))/900"); ...
             str2sym("(322+13*sqrt(70))/900"); ...
             str2sym("128/225"); ...
             str2sym("(322+13*sqrt(70))/900"); ...
             str2sym("(322-13*sqrt(70))/900")];
    end
end